import java.util.*;
public class StringConcat
{
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter string 1:");
        String s1=sc.next();
        System.out.print("Enter string 2:");
        String s2=sc.next();
        System.out.println("Concatenated string:"+s1+s2);
    }
}